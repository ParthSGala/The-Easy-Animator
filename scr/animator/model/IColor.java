package animator.model;

/**
 * the interface represents the color properties.
 */
public interface IColor {

  /**
   * the method gets red color of the shape.
   *
   * @return the shape color
   */
  public int getRed();

  /**
   * the method gets blue color of the shape.
   *
   * @return the shape color
   */
  public int getBlue();

  /**
   * the method gets green color of the shape.
   *
   * @return the shape color
   */
  public int getGreen();
}
