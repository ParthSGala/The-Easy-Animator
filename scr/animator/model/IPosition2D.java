package animator.model;

/**
 * the interface represents the position properties.
 */
public interface IPosition2D {

  /**
   * the method to get the position x.
   *
   * @return position x
   */
  public int getX();

  /**
   * the method to get the position y.
   *
   * @return position y
   */
  public int getY();
}
